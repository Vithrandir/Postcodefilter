def process(adress):
	dicNA = {'N/A': {'所属省份': '', '所辖县级市': [], '邮编': ''}}
	adress_temp = adress
	for province in dic.keys():

		if adress.startswith(province):

			dicNA['N/A']['所属省份'] = province
			adress = adress[len(province):]

			if province.endswith("省") == False and province.endswith("市") == False and (adress.startswith("省") == True or adress.startswith("市") == True):

				adress = adress[1:]

			for city in dic[province]["下辖地级市"].keys():

				if adress.startswith(city):
					code = dic[province]["下辖地级市"][city]["邮编"]
					return province,city,code

				elif province == "北京" or province == "北京市" or province == "上海" or province == "上海市" or province == "天津市" or province == "天津" or province == "重庆市" or province == "重庆":
					code = dic[province]["下辖地级市"][city]["邮编"]
					return province,city,code

				else:
					county_level_cities = dic[province]["下辖地级市"][city]["下辖县级市"]

					for county_level_city in county_level_cities:

						if adress.startswith(county_level_city):

							code = dic[province]["下辖地级市"][city]["邮编"]
							return province,city,code

		else:

			for city in dic[province]["下辖地级市"].keys():

				if adress.startswith(city):

					code = dic[province]["下辖地级市"][city]["邮编"]
					return province,city,code

				for county_level_city in dic[province]["下辖地级市"][city]["下辖县级市"]:

					if adress.startswith(county_level_city):

						code = dic[province]["下辖地级市"][city]["邮编"]
						return province,city,code


