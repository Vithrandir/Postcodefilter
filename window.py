def adressFormat(adress):
	another_adress = adress.strip()
	another_adress = another_adress.replace(" ","")
	another_adress = another_adress.replace("\t","")
	another_adress = another_adress.replace("中国","")
	another_adress = another_adress.replace("，","")
	another_adress = another_adress.replace(",","")
	for i in range(0,32):
		another_adress = another_adress.replace(chr(i),"")
	another_adress = another_adress.replace("\u3000","")

	return another_adress

